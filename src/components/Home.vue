      <li @click="setSlide(4)"><span></span><span></span></li>
    </ul>
  </div>

  <div class="next" @click="nextPage()"><span class="text">{{nextButton[index]}}</span><span class="discover">Discover</span></div>

</div>
</template>

<script>
var jQuery = require('jquery-easing')

var focus = true
var slideTransitionTime = 6000

export default {
  name: 'main',
  data() {
    return {
      isInit: false,
      frame: '',
      slides: ['one', 'two', 'three', 'four', 'five'],
      nextButton: ['Home', 'Clients', 'Services', 'About', 'Contact'],
      index: 0
    }
  },
  methods: {
    setSlide: function(slide) {
      // Reset auto slider
      clearInterval(window.sliderInterval)
      window.sliderInterval = null
      // Goto slide
      this.doSlideTransition(slide)
    },
    floatThis: function(element) {
      jQuery(window).bind('mousemove', function(e) {
        var moveX = -(e.pageX - jQuery(window).width() / 2) / 12
        var moveY = -(e.pageY - jQuery(window).height() / 2) / 12
        jQuery(element).css({
          'transform': 'translate(' + moveX + 'px,' + moveY + 'px)'
        })
      })
      jQuery(element).addClass('ease')
    },
    doSlideTransition: function(slide) {
      var main = this
      // Reset jquery events
      jQuery('.elem-two, .elem-five-2').attr('style', '').removeClass('ease')
      jQuery(window).unbind('mousemove')
      if (slide != 3) {
        jQuery('.elem-four.float').on('webkitAnimationIteration', function() {
          jQuery('.elem-four').removeClass('float').off('webkitAnimationIteration')
        })
      }
      // Discover button animation
      jQuery('.next .text').addClass('swim')
      var discoverTimer = setTimeout(function() {
        jQuery('.next .text').removeClass('swim')
      }, 450)
      var discoverButtonTimer = setTimeout(function() {
        jQuery('.next .text').html(main.nextButton[slide])
      }, 225)
      // Add jquery events
      switch (slide) {
        case 1:
          var bulletTimer = setTimeout(function() {
            main.floatThis(jQuery('.elem-two'))
          }, 1500)
          break
        case 3:
          var bulletTimer = setTimeout(function() {
            jQuery('.elem-four').addClass('float')
          }, 2100)
          break
        case 4:
          var bulletTimer = setTimeout(function() {
            main.floatThis(jQuery('.elem-five-2'))
          }, 1500)
          break
      }
      // Add class to main
      this.frame = this.slides[slide]
    },
    nextPage: function() {
      var main = this

      switch (this.frame) {
        case 'two':
          main.$router.push('/clients')
          break
        case 'three':
          main.$router.push('/services')
          break
        case 'four':
          main.$router.push('/about')
          break
        case 'five':
          main.$router.push('/contact')
          break
      }
    },
    windowFocus: function() {
      var main = this
      jQuery(window).one('focus', function() {
        if (!window.sliderInterval) {
          window.sliderInterval = setInterval(function() {
            if (window.homeIndex < main.slides.length - 1) window.homeIndex++
              else window.homeIndex = 0
            main.doSlideTransition(window.homeIndex)
          }, slideTransitionTime)
        }
        main.windowBlur()
      })
    },
    windowBlur: function() {
      var main = this
      jQuery(window).one('blur', function() {
        clearInterval(window.sliderInterval)
        window.sliderInterval = null
        //main.windowFocus()
        main.windowBlur()
      })
    }
  },
  beforeMount() {
    this.index = window.homeIndex
  },
  mounted() {
    var main = this

    var mountTimer = setTimeout(function() {
      main.isInit = true
      main.frame = main.slides[window.homeIndex]

      // Start slider
      if (document.hasFocus()) {
        if (!window.sliderInterval) {
          window.sliderInterval = setInterval(function() {
            if (window.homeIndex < main.slides.length - 1) window.homeIndex++
              else window.homeIndex = 0
            main.doSlideTransition(window.homeIndex)
          }, slideTransitionTime)
        }
        main.windowBlur()
      } else {
        main.windowFocus()
      }

      // Mobile section
      document.getElementById('main').addEventListener('touchstart', handleTouchStart, false)
      document.getElementById('main').addEventListener('touchmove', handleTouchMove, false)
      var xDown = null
      var yDown = null

      function handleTouchStart(evt) {
        xDown = evt.touches[0].clientX
        yDown = evt.touches[0].clientY
        // Stop slider
        clearInterval(window.sliderInterval)
        window.sliderInterval = null
      }

      function handleTouchMove(evt) {
        if (!xDown || !yDown) {
          return;
        }
        var xUp = evt.touches[0].clientX
        var yUp = evt.touches[0].clientY
        var xDiff = xDown - xUp;
        var yDiff = yDown - yUp;
        if (Math.abs(xDiff) > Math.abs(yDiff)) {
          if (xDiff > 0) {
            /* left swipe */
            if (window.homeIndex < main.slides.length - 1) window.homeIndex++
              else window.homeIndex = 0
            main.doSlideTransition(window.homeIndex)
          } else {
            /* right swipe */
            if (window.homeIndex > 0) window.homeIndex--
              else window.homeIndex = 4
            main.doSlideTransition(window.homeIndex)
          }
        } else {
          if (yDiff > 0) {
            /* up swipe */
          } else {
            /* down swipe */
          }
        }
        xDown = null
        yDown = null
      }

      // pixel init
      this.$analytics.fbq.event('PageView')
    }, 1000)
  }
}
</script>
